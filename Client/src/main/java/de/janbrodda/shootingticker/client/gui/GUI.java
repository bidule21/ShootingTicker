	public int getRemainingSeconds() {
		int timeFieldMinutes = Integer.parseInt(timeMinutesField.getText());
		int timeFieldSeconds = Integer.parseInt(timeSecondsField.getText());
		
		return timeFieldMinutes * 60 + timeFieldSeconds;
	}
	
	public void setRemainingSeconds(int seconds) {
		int timeFieldMinutes = seconds / 60;
		int timeFieldSeconds = seconds % 60;
		
		timeSecondsField.setText(timeFieldSeconds + "");
		timeMinutesField.setText(timeFieldMinutes + "");
	}
	
	public void startRemainingSecondsTimer() {
		timer.scheduleAtFixedRate(remainingTimeUpdater, 0L, 1 * 1000);
	}
	
	public void stopRemainingSecondsTimer() {
		remainingTimeUpdater.cancel();
	}
	
	public void showPopup(String message) {
		JOptionPane.showMessageDialog(this, message, "", JOptionPane.INFORMATION_MESSAGE);
	}
	
	public void executeInBackground(final Runnable runnable) {
		mainContent.add(loadingPanel, 0);
		mainContent.moveToFront(loadingPanel);
		mainContent.moveToBack(jTabbedPane1);
		
		new Thread() {
			@Override
			public void run() {
				runnable.run();
				mainContent.remove(loadingPanel);
				frame.repaint();
			}
		}.start();
	}

