    private void createCompetitionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createCompetitionButtonActionPerformed
		int year = Integer.parseInt(newCompetitionDateYearField.getText());
		int month = Integer.parseInt(newCompetitionDateMonthField.getText());
		int day = Integer.parseInt(newCompetitionDateDayField.getText());
		int hour = Integer.parseInt(newCompetitionDateHourField.getText());
		int minute = Integer.parseInt(newCompetitionDateMinuteField.getText());
		Calendar calendar = Calendar.getInstance();
		calendar.set(year, month - 1, day, hour, minute, 0);
		Date date = calendar.getTime();
		
		final Competition competition = new Competition();
		competition.name = newCompetitionNameField.getText();
		competition.date = date;
		competition.numShots = 0;
		
		executeInBackground(new Runnable() {
			@Override
			public void run() {
				try {
					app.saveCompetition(competition);
					panelChangedToCompetitions();
				} catch (IOException ex) {
					Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
					showPopup("Can't send Competition to Server");
				}
			}
		});
    }//GEN-LAST:event_createCompetitionButtonActionPerformed

    private void startUploadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startUploadButtonActionPerformed
		ComboboxItem<Competition> selectedCompetitionItem = (ComboboxItem<Competition>) remoteCompetitionDropdown.getSelectedItem();
		ComboboxItem<File> selectedFileItem = (ComboboxItem<File>) localCompetitionDropdown.getSelectedItem();
		
		int numShots = Integer.parseInt(numShotsField.getText());
		int remainingSeconds = getRemainingSeconds();
		
		if (selectedCompetitionItem != null && selectedFileItem != null && remainingSeconds > 0 && numShots > 0) {
			
			Competition competition = selectedCompetitionItem.value;
			competition.numShots = numShots;
			competition.remainingSeconds = remainingSeconds;
			app.selectRemoteCompetition(competition);
			
			File selectedFile = selectedFileItem.value;
			app.selectLocalCompetitionFolder(selectedFile);
			
			app.startCompetitionUpload();
		} else {
			showPopup("Please Input all necessary Data..");
		}
    }//GEN-LAST:event_startUploadButtonActionPerformed

